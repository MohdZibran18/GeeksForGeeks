class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
       // approach 1 
       // we can use ordered set and return the last element 
       set<int>ans;
       int ele=-1;
       for(int i=0;i<arr.size();i++){
           ans.insert(arr[i]);
       }
       if(ans.size()<2){
           return ele;
       }
       return *next(ans.begin(),ans.size()-2);
    }
};

