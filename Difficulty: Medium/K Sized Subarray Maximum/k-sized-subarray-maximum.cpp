class Solution {
  public:
    vector<int> maxOfSubarrays(vector<int>& arr, int k) {
        // code here
        multiset<int>mul;
        int j=0;
        vector<int>ans;
        for(int i=0;i<arr.size();i++){
            
            mul.insert(arr[i]);
            if(i>=k-1){
                int ele=*mul.rbegin();
                ans.push_back(ele);
                mul.erase(mul.find(arr[j]));
                j++;
            }
        }
        return ans;
       
    }
};


